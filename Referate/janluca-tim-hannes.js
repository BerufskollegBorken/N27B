 console.log("Die Schleife startet mit dem Wert 90 und es wird in 7er-Schritten heruntergezäht, solange der Wert größer als i ist.");

 for (var i = 90; i > 0; i -=7 ){
    console.log(i);
 }


console.log("Bilde eine Schleife, die von drei herunterzählt:");

 for (var i = 3; i > 0; i-- ){
    console.log(i);
 }

console.log("Es soll von 6 bis 9 hochgezählt werden, danach wieder herunter:")

 for (var i = 6; i < 10; i++ ){
    console.log(i);
 }
 for (i = 9; i >= 6; i-- ){
    console.log(i);
 }

